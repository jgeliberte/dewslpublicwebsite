			<p><b>The early warning system for landslides follows a protocol that derives early warning information from sensor data and surficial ground data.</b></p>
			<p><b>Landslide sensors are a Filipino-made, solar-powered, early warning instrument that is able to detect and monitor ground movement, soil moisture, and is equipped with a rain gauge. Sensors send data to the PHIVOLCS headquarters every 30 minutes, 24 hours-7 days a week.</b></p><br>
			<p><b>The LEWC is composed of members of the community at risk of landslide. They are the projects's partners on-the-ground who participate in learning about the landslide risk, monitoring and warning through measuring observation markers for ground movement, disseminating and communicating early warning information, and response.</b></p>
			<p><b>Each project stakeholder, including PHIVOLCS, the LEWC and the community, and the local government units of the barangay, municpal or city, and province plays an active role in the EWS-L protocol. Early warning information on the current alert level status of the site and the corresponding recommended response is communicated among all stakeholders.</b></p>
