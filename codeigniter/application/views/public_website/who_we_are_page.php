			<p><b>In 2006, after weeks of continuous rain and an earthquake, a catastrophic landslide occured in Brgy. Guinsaugon, St. Bernard, Southern Leyte. The entire village of Guinsaugon was buried, killing over a thousand people, and destroying homes and livelihood.</b></p>
			<br>
			<p><b>The Guinsaugon Tragedy inspired the research beginnings of an early warning system for landslides. From 11 sites, the research expanded to sites in Luzon, Visayas, and Mindanao, each with an installed landslide sensor and an organized landslide early warning committee.</b></p>
